int	ft_toupper(int c)
{
	unsigned char	ch;

	if (c > 255 || c < 0)
		return (c);
	ch = (unsigned char)c;
	if (ch >= 'a' && ch <= 'z')
		ch -= 32;
	return (ch);
}
