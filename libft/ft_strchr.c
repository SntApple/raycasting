#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*p;

	if (c < -128 || c > 127)
		return (0);
	p = (char*)s;
	while (*p)
	{
		if (*p == (char)c)
			return (p);
		p++;
	}
	if (c == '\0')
		return (p);
	else
		return (0);
}
