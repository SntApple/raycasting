int	ft_tolower(int c)
{
	unsigned char	ch;

	if (c > 255 || c < 0)
		return (c);
	ch = (unsigned char)c;
	if (ch >= 'A' && ch <= 'Z')
		ch += 32;
	return (ch);
}
